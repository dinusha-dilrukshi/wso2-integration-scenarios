package org.wso2.carbon.identity.cloud.sample;

public class PlanYourTripWebConfiguration {

    private static final PlanYourTripWebConfiguration instance = new PlanYourTripWebConfiguration();

    private String serverURL;
    private String tokenURL;
    private String consumerKey;
    private String consumerSecret;

    private PlanYourTripWebConfiguration() {

    }

    public static PlanYourTripWebConfiguration getInstance() {
        return instance;
    }

    public String getServerURL() {
        return serverURL;
    }

    public void setServerURL(String serverURL) {
        this.serverURL = serverURL;
    }

    public String getTokenURL() {
        return tokenURL;
    }

    public void setTokenURL(String tokenURL) {
        this.tokenURL = tokenURL;
    }

    public String getConsumerKey() {
        return consumerKey;
    }

    public void setConsumerKey(String consumerKey) {
        this.consumerKey = consumerKey;
    }

    public String getConsumerSecret() {
        return consumerSecret;
    }

    public void setConsumerSecret(String consumerSecret) {
        this.consumerSecret = consumerSecret;
    }
}
